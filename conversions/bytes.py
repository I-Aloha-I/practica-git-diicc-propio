#Bytes a Kilobytes: KB = bytes รท 1024
#Kilobytes a Megabytes: MB = KB รท 1024

def bytes_to_kilobytes(bytes: float) -> float:
    """Convierte bytes a kilobytes"""
    return bytes / 1024

def kilobytes_to_Megabytes(kilobytes: float) -> float:
    """Convierte kilobytes a megabytes"""
    return kilobytes / 1024